// DESCRIPTION:
//
//     Implement a function which convert the given boolean value into its string representation.
//
//     Note: Only valid inputs will be given.

// SOLUTION

function booleanToString(b){
    return b.toString();
}

console.log(booleanToString("true sjdfhkdslfm dfgfdgf"));
console.log(booleanToString(" sjdfhkdslfm dfgfdgf false"));
console.log(booleanToString(" sjdfhkdslfm dfgfdgf"));